export const postAddRole = async (accessToken, id) => {
  const config = {
    url: `${apiServerUrl}/api/messages/role`,
    method: "POST",
    data: {
      'userId': id
    },
    headers: {
      "content-type": "application/json",
      Authorization: `Bearer ${accessToken}`,
    },
  };
  const {data, error} = await callExternalApi({config});
  return {
    data: data || null,
    error,
  };
}
